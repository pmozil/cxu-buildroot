static const uint8_t Rcon[11] = {
  0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1B,0x36
};

static void KeyExpansion(const uint8_t key[16], uint8_t roundKeys[176]) {
  memcpy(roundKeys, key, 16);
  uint32_t bytesGenerated = 16;
  int rconIteration = 1;
  uint8_t temp[4];

  while (bytesGenerated < 176) {
    for (int i = 0; i < 4; ++i) temp[i] = roundKeys[bytesGenerated - 4 + i];

    if (bytesGenerated % 16 == 0) {
      // rotate
      uint8_t t = temp[0];
      temp[0] = temp[1];
      temp[1] = temp[2];
      temp[2] = temp[3];
      temp[3] = t;
      // sub bytes
      temp[0] = sbox[temp[0]];
      temp[1] = sbox[temp[1]];
      temp[2] = sbox[temp[2]];
      temp[3] = sbox[temp[3]];
      // Rcon
      temp[0] ^= Rcon[rconIteration];
      rconIteration++;
    }
    for (int i = 0; i < 4; ++i) {
      roundKeys[bytesGenerated] = roundKeys[bytesGenerated - 16] ^ temp[i];
      bytesGenerated++;
    }
  }
}

static void AddRoundKey(uint8_t state[16], const uint8_t *roundKey) {
  for (int i = 0; i < 16; ++i) state[i] ^= roundKey[i];
}

static void SubBytes(uint8_t state[16]) {
  for (int i = 0; i < 16; ++i) state[i] = sbox[state[i]];
}

static void ShiftRows(uint8_t state[16]) {
  // Row 0 unchanged
  uint8_t tmp[16];
  tmp[0] = state[0];
  tmp[1] = state[5];
  tmp[2] = state[10];
  tmp[3] = state[15];

  tmp[4] = state[4];
  tmp[5] = state[9];
  tmp[6] = state[14];
  tmp[7] = state[3];

  tmp[8] = state[8];
  tmp[9] = state[13];
  tmp[10] = state[2];
  tmp[11] = state[7];

  tmp[12] = state[12];
  tmp[13] = state[1];
  tmp[14] = state[6];
  tmp[15] = state[11];

  memcpy(state, tmp, 16);
}

static void MixColumns(uint8_t state[16]) {
  for (int c = 0; c < 4; ++c) {
    int col = c * 4;
    uint8_t a0 = state[col + 0];
    uint8_t a1 = state[col + 1];
    uint8_t a2 = state[col + 2];
    uint8_t a3 = state[col + 3];

    // multiply by constants in AES matrix:
    // [2 3 1 1]
    // [1 2 3 1]
    // [1 1 2 3]
    // [3 1 1 2]

    uint8_t m20 = gf8_mul(2, a0); // 2*a0
    uint8_t m21 = gf8_mul(2, a1);
    uint8_t m22 = gf8_mul(2, a2);
    uint8_t m23 = gf8_mul(2, a3);

    uint8_t m30 = gf8_mul(3, a0); // 3*a0
    uint8_t m31 = gf8_mul(3, a1);
    uint8_t m32 = gf8_mul(3, a2);
    uint8_t m33 = gf8_mul(3, a3);

    uint8_t r0 = m20 ^ m31 ^ a2 ^ a3;
    uint8_t r1 = a0 ^ m21 ^ m32 ^ a3;
    uint8_t r2 = a0 ^ a1 ^ m22 ^ m33;
    uint8_t r3 = m30 ^ a1 ^ a2 ^ m23;

    state[col + 0] = r0;
    state[col + 1] = r1;
    state[col + 2] = r2;
    state[col + 3] = r3;
  }
}

static void AES128_EncryptBlock(const uint8_t in[16], uint8_t out[16], const uint8_t roundKeys[176]) {
  uint8_t state[16];
  memcpy(state, in, 16);

  AddRoundKey(state, roundKeys);

  for (int round = 1; round <= 9; ++round) {
    SubBytes(state);
    ShiftRows(state);
    MixColumns(state);
    AddRoundKey(state, roundKeys + 16*round);
  }

  // final round (no MixColumns)
  SubBytes(state);
  ShiftRows(state);
  AddRoundKey(state, roundKeys + 160);

  memcpy(out, state, 16);
}

static void print_hex(const uint8_t *b, size_t n) {
  for (size_t i = 0; i < n; ++i) printf("%02x", b[i]);
  printf("\n");
}

static inline uint64_t rdcycle(void) {
    uint32_t lo, hi;
    asm volatile ("rdcycleh %0" : "=r"(hi));
    asm volatile ("rdcycle %0"  : "=r"(lo));
    return ((uint64_t)hi << 32) | lo;
}

static inline uint64_t rdinstret(void) {
    uint32_t lo, hi;
    asm volatile ("rdinstreth %0" : "=r"(hi));
    asm volatile ("rdinstret %0"  : "=r"(lo));
    return ((uint64_t)hi << 32) | lo;
}

void aes_sw_demo(void) {
  uint8_t key[16] = {
    0x00,0x01,0x02,0x03,0x04,0x05,0x06,0x07,
    0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f
  };
  uint8_t pt[16] = {
    0x00,0x11,0x22,0x33,0x44,0x55,0x66,0x77,
    0x88,0x99,0xaa,0xbb,0xcc,0xdd,0xee,0xff
  };
  uint8_t expected_ct[16] = {
    0x69,0xc4,0xe0,0xd8,0x6a,0x7b,0x04,0x30,
    0xd8,0xcd,0xb7,0x80,0x70,0xb4,0xc5,0x5a
  };

  printf("Running AES-128 test with CXU enabled\n");

  uint32_t lo = rdcycle();

  uint8_t roundKeys[176];
  KeyExpansion(key, roundKeys);

  uint8_t ct[16];
  AES128_EncryptBlock(pt, ct, roundKeys);

  uint32_t hi = rdcycle();

  printf("Timer0 ticks taken = %ld\n", hi - lo);

  printf("Plaintext:  ");
  print_hex(pt, 16);
  printf("Key:        ");
  print_hex(key, 16);
  printf("Ciphertext: ");
  print_hex(ct, 16);
  printf("Expected:   ");
  print_hex(expected_ct, 16);

  if (memcmp(ct, expected_ct, 16) == 0) {
    printf("SUCCESS: ciphertext matches expected vector.\n");
  } else {
    printf("FAIL: ciphertext does NOT match.\n");
  }
}

